const LANGUAGE_CONTEXT = new WeakMap();

function normalizeShopperLanguage(value = '') {
  const raw = String(value || '').toLowerCase();
  if (raw.includes('amharic') || raw === 'am') return 'amharic';
  return 'english';
}

function setShopperLanguageContext(target, language = '') {
  if (target && typeof target === 'object') {
    LANGUAGE_CONTEXT.set(target, normalizeShopperLanguage(language));
  }
  return target;
}

function shopperLanguage(client = {}) {
  if (client && typeof client === 'object' && LANGUAGE_CONTEXT.has(client)) {
    return LANGUAGE_CONTEXT.get(client);
  }
  return normalizeShopperLanguage(client?.shopperLanguage || client?.shopperLanguagePreference || '');
}

function interpolate(template, vars = {}) {
  return String(template || '').replace(/\{([a-zA-Z0-9_]+)\}/g, (_, key) =>
    vars[key] === undefined || vars[key] === null ? `{${key}}` : String(vars[key])
  );
}

function shopperText(client, key, vars = {}, fallback = '') {
  const lang = shopperLanguage(client);
  const source = lang === 'english' ? ENGLISH : AMHARIC;
  return interpolate(source[key] || fallback || key, vars);
}

function inferButtonKey(button = {}) {
  const text = String(button.text || '').toLowerCase();
  const data = String(button.callback_data || '');
  if (button.copy_text) return 'BTN_COPY_TRACKING';
  if (data.includes(':main_menu')) return 'BTN_MAIN_MENU';
  if (data.includes(':back_order')) return 'BTN_BACK';
  if (data.includes(':language:english')) return 'BTN_LANGUAGE_ENGLISH';
  if (data.includes(':language:amharic')) return 'BTN_LANGUAGE_AMHARIC';
  if (data.includes(':track_order')) return text.includes('delivery') ? 'BTN_TRACK_DELIVERY' : 'BTN_TRACK_ORDER';
  if (data.includes(':support')) return 'BTN_TALK_SUPPORT';
  if (data.includes(':payment_proof')) return 'BTN_PAYMENT_PROOF';
  if (data.includes(':search')) return text.includes('again') ? 'BTN_SEARCH_AGAIN' : 'BTN_SEARCH';
  if (data.includes(':explore')) {
    if (text.includes('back')) return 'CATALOG_BACK_CATEGORIES';
    if (text.includes('categor')) return 'BTN_BROWSE_CATEGORIES';
    return 'BTN_BROWSE_PRODUCTS';
  }
  if (data.includes(':order:') || data.includes(':recommend_order') || data.includes(':intent_continue')) return 'BTN_ORDER_THIS';
  if (data.includes(':gallery')) return 'BTN_VIEW_MORE_PHOTOS';
  if (data.includes(':cancel_order')) return 'BTN_CANCEL_ORDER';
  if (data.includes(':edit_order')) return 'BTN_EDIT_INFO';
  if (data.includes(':confirm_order')) return 'BTN_CONFIRM_ORDER';
  if (data.includes(':promo_code')) return 'BTN_ADD_PROMO';
  if (data.includes(':skip_promo')) return 'BTN_SKIP_PROMO';
  if (data.includes(':match_add')) return 'BTN_INCLUDE_MATCH';
  if (data.includes(':match_skip')) return 'BTN_NO_THANKS_PAYMENT';
  if (data.includes(':recommend_view') || data.includes(':intent_view')) return 'BTN_VIEW_PRODUCT';
  if (data.includes(':recommend_later') || data.includes(':intent_later')) return 'BTN_NOT_NOW';
  if (data.includes(':recommend_stop')) return 'BTN_STOP_SUGGESTIONS';
  if (data.includes(':intent_similar')) return 'BTN_SEE_SIMILAR';
  if (data.includes(':intent_stop')) return 'BTN_STOP_REMINDERS';
  if (data.includes(':campaign_view')) return 'BTN_VIEW_OFFERS';
  if (data.includes(':campaign_stop')) return 'BTN_STOP_PROMOTIONS';
  if (data.includes(':delivery_received')) return 'BTN_RECEIVED';
  if (data.includes(':delivery_not_received')) return 'BTN_NOT_RECEIVED';
  if (text.includes('prev page')) return 'BTN_PREV_PAGE';
  if (text.includes('next page')) return 'BTN_NEXT_PAGE';
  if (text.includes('maybe later')) return 'BTN_MAYBE_LATER';
  return '';
}

function localizeButtons(client, rows = []) {
  if (shopperLanguage(client) === 'english') return rows;
  return rows.map(row => row.map(button => {
    const key = inferButtonKey(button);
    return key ? { ...button, text: shopperText(client, key, {}, button.text) } : button;
  }));
}

function localizeResult(client, result) {
  if (!result || shopperLanguage(client) === 'english') return result;
  if (Array.isArray(result.buttons)) result.buttons = localizeButtons(client, result.buttons);
  if (Array.isArray(result.batchProducts)) {
    result.batchProducts = result.batchProducts.map(item => ({
      ...item,
      buttons: Array.isArray(item.buttons) ? localizeButtons(client, item.buttons) : item.buttons
    }));
  }
  return result;
}

